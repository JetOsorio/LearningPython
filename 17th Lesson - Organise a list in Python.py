months = ['march', 'april', 'december', 'august']
months.sort()  # sorts months in alphabetical order
print(months)

months = ['march', 'april', 'december', 'august']
months.sort(reverse=True)  # sorts months alphabetically, but from z-a
print(months)

months = ['march', 'april', 'december', 'august']
print(sorted(months))  # temporarily sorts list
print(months)

days = ['monday', 'tuesday', 'wednesday', 'thursday', 'friday', 'saturday', 'sunday']
days.reverse()  # reverses order of list
print(days)
