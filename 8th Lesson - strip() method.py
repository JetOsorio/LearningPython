address = "         1/3 Shedworth St           "
print(address)

# strip() removes blank space at beginning and end of string
address = "         1/3 Shedworth St           "
print(address.strip())

# lstrip removes blank space on left
address = "         1/3 Shedworth St           "
print(address.lstrip())

# rstrip removes blank space on right
address = "         1/3 Shedworth St           "
print(address.rstrip())