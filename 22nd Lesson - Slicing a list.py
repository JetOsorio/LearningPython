names = ['Bob', 'john', 'adam', 'carol', 'jason']
print(names[0:2])  # Prints names between index 0 and 2, excluding index 2
print(names[1:3])  # Prints names between index 1 to 3, excluding index 3
print(names[:3])  # Prints names from first index until index 3
print(names[2:]) # Prints names from index 2 until the last index
print(names[-3:]) # Prints last 3 items in index
